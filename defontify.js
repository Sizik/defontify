var defontify = function(parentNode){
    for(var i = parentNode.childNodes.length-1; i >= 0; i--){
        var node = parentNode.childNodes[i];

        //  Make sure this is a text node

        if(node.nodeType == Element.TEXT_NODE){
            var newText = '';
            
            for(var c = 0; c < node.textContent.length; c++) {
                var codepoint = node.textContent.charAt(c);
                
                // Need to handle surrogate pair for codepoints outside the BMP
                if(codepoint >= UTF16SurrogateStart 
                && codepoint <= UTF16SurrogateEnd) {     
                    codepoint = node.textContent.substring(c, c+2);
                    c++;
                }
                
                if(codepoint in fontDict) {
                    newText += fontDict[codepoint];
                } else {
                    newText += codepoint;
                }
            }
            
            
            node.textContent = newText;
        } else if(node.nodeType == Element.ELEMENT_NODE){
            //  Check this node's child nodes for text nodes to act on

            defontify(node);
        }
    }
};

